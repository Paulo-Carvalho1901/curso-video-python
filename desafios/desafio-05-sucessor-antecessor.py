"""
Faça um programa que que leia um numero inteiro
e mostre na tela seu sucessor e seu antecessor

"""

n = int(input('Digite um número: '))
a = n -1
s = n +1

print(f'Analisando seu valor {n} seu antecessor é {a} e seu sucessor é {s} ')

